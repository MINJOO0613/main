# 문제 1 : for문
num = input("0 이상의 수를 입력하세요:")          # 숫자를 입력하면, 'num'으로 받도록 함. 입력하는 수가 "*"이 생성되는 행의 개수를 의미함. ex. 3을 입력하면 3줄의 *이 생성
a = '*'                                       # "*"을 a로 저장함. (이떄, a는 str 타입임)

if int(num) > 0:                              # num이 자연수가 아닐 경우, '*'이 생성되지 않는 조건 걸어둠
    for j in range(1,2*int(num)):             # 1+3+5+7와 같이 *이 생성될 수 있도록 범위 설정함 (2a-1과 같음)
        if j % 2 == 1:                        # 홀수만 반환함
            result = a*j                      # str에 int를 곱할 경우, 숫자만큼 문자를 생성함
            print("{0:^10}".format(result))  # for 반복문 안에 print를 넣어 설정헌 범위만큼 "*"이 생성됨
                                              # 가운데 정렬을 하기 위해 "{0:<10}".format("*")"를 사용함

#정답(경원튜터님)
for i in range(num):
    print(' ' * (num - i - 1) + '*' * (2 * i + 1))

