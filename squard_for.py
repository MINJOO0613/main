# 문제 3 : 각 요소 다 더하기(1 + 2 + 3 + 4....)
# 예시 입력 값: a = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]      # 출력 값: 45
def sum_ls(n):                              #리스트의 요소를 모두 더하는 함수 "sum_ls"로 정의
    result = 0                              #모두 더한 값을 받을 변수 필요하여 result(type:int)로 생성
    for i in n:                             #리스트 안의 요소들을 모두 꺼냄
        if type(i) == int:                  #꺼낸 요소가 리스트인지, 숫자인지 구분 필요함
            result += i                     #꺼낸 요소가 숫자라면, 모두 합하여 result 변수에 저장함
        else:                               #꺼낸 요소가 리스트라면,
            for j in i:                     #다시 리스트 안의 요소들을 모두 꺼냄
                result += j                 #요소들을 모두 합하여 result 변수에 저장함
    return result                           #result의 값이 반환됨

a = [[1, 2, 3], [4, 5, 6],[7, 8, 9]]
print(sum_ls(a))                        # 45
print(sum_ls([1,3,5]))                  # 9
print(sum_ls([[1],[2]]))                # 3

