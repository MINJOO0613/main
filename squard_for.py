# 문제 4 : 두 개의 리스트의 모든 요소를 덧셈 후 결과를 출력해주세요   참고) - 1+4, 1+5, 1+6, 2+4……
# 입력 값
# list1 = [1, 2, 3]
# list2 = [4, 5, 6]
# 결과 값) 5 6 7 6 7 8 7 8 9
def solution(a, b):                     #리스트1을 a로, 리스트2를 b로 받아 각 요소들을 모두 더하는 함수 정의
    ls = []                             #빈 리스트 생성
    for i in a:                         # a리스트의 요소들을 모두 꺼냄
        for j in b:                     # b리스트의 요소들을 모두 꺼냄
            ls.append(i+j)              # a의 요소 + b의 요소를 모두 각각 더해 ls에 저장
    return (" ".join(map(str, ls)))     # ls리스트의 요소들을 str형식으로 바꿔준 후, " "공백을 추가하여 문자열로 합친다.

list1 = [1, 2, 3]
list2 = [4, 5, 6]
print(solution(list1, list2))           #5 6 7 6 7 8 7 8 9
print(solution([1,10],[2,-1]))   #3 0 12 9